<div class="cover">
  <h2>Dónde comer en Tijuana?</h2>
</div>
<p>
  Tijuana es una ciudad en crecimiento, actualmente cuenta con varias zonas que ofrecen una gran variedad de comida.

  Aquí dejo 5 zonas en las que podrás encontrar buenos lugares dónde comer.
</p>

<h3>Playas de Tijuana</h3>
<p>
  Además de contar con opciones de comida se cuenta con la playa, la cual siempre es buena para una caminata, especialmente durante el atardecer. Si buscas de un ambiente relajado y precios razonables, Playas es la opción.
</p>

<h3>Zona Centro</h3>
<p>
  El centro es un punto de folklore, donde además de opciones de comida se puede disfrutar de una caminata por la emblemática calle revolución. Es una zona de contrastes que bien define una de las caras de Tijuana. Si estas en busca de algo alternativo esta es la zona.
</p>

<h3>Zona Rio</h3>
<p>
  Zona rio se caracteriza por su urbanismo donde se encuentran restaurantes de renombre, adicionalmente hay varios centros comerciales o zonas de entretenimiento para continuar la vuelta. Si estas en busca de una opción más elocuente esta es la zona.
</p>

<h3>Insurgentes</h3>
<p>
  Esta zona ha crecido mucho en los últimos años y de igual forma lo ha hecho su oferta gastronómica. Podría parecer lejos para algunas personas, sin embargo vale la pena, pues las opciones son buenas al igual que los precios.
</p>

<h3>El Valle de Guadalupe</h3>
<p>
  El valle se encuentra en Ensenada. El valle es una zona entre montañas que cuenta con su propio clima. La oferta gastronómica es más sofisticada y en general los precios son altos, sin embargo la experiencia lo vale.
</p>
