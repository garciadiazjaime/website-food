<h1>Cómo aparecer en el directorio</h1>

<p>Puedes aparecer en nuestro directorio haciendo un post de Instagram incluyendo #frescomer. Así de sencillo.</p>
<p>Para mejorar tu ranking así como tu presencia en el directorio puedes hacer lo siguiente:</p>
<ul>
	<li>Incluye un teléfono en el post o en tu info de perfil. (Si incluyes más de uno, solo vamos a mostrar el primero)</li>
	<li>Cuando publiques, asegúrate de incluir la locación. Estamos trabajando para poder filtrar por distancia.</li>
	<li>Incluye las palabras claves que sean relevantes de la siguiente lista:
		<ul>
			<li>Desayuno</li>
			<li>Lunch</li>
			<li>Café</li>
			<li>Veggie</li>
		</ul>
	</li>
	<li>Usa el #frescomer en todos tus posts. Parte de nuestro algoritmo prioritiza posts recientes</li>
</ul>

